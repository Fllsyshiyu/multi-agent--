# ── 4. Position Card ────────────────────────────────────────────────────────

@dataclass
class PositionCard:
    """立场陈述卡 — each agent's structured stance output.

    JSON Schema (from doc Table 0, row 4):
    {
      "agent_id": "resident_parent_01",
      "stakeholder_group": "育儿家庭",
      "stance": "conditional_support",
      "claims": ["具体主张1", "具体主张2"],
      "evidence_ids": ["E-03", "E-12"],
      "non_negotiables": ["不可退让的底线"],
      "risks": ["风险1"],
      "suggested_changes": ["对方案的修改建议"],
      "confidence": 0.72
    }
    """
    agent_id: str = ""
    stakeholder_group: str = ""
    stance: Stance = Stance.NEUTRAL
    claims: list[str] = field(default_factory=list)
    evidence_ids: list[str] = field(default_factory=list)
    non_negotiables: list[str] = field(default_factory=list)
    risks: list[str] = field(default_factory=list)
    suggested_changes: list[str] = field(default_factory=list)
    confidence: float = 0.5
    round_no: int = 1
    # SOP v1.2 tracking
    artifact_id: str = ""
    stage_id: str = ""
    produced_by: str = ""
    input_refs: list[str] = field(default_factory=list)
    evidence_refs: list[str] = field(default_factory=list)
    validation_status: str = "pending"


