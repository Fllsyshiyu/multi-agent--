"""Structured deliberation artifacts — 7 artifact types per MetaGPT+Urban Planning SOP.

All agents output these structured artifacts, NOT free-form chat messages.
This ensures downstream agents can consistently read, compare, score, and persist outputs.
"""

from __future__ import annotations

from dataclasses import dataclass, field
from enum import Enum
from typing import Optional


class Stance(str, Enum):
    SUPPORT = "support"
    CONDITIONAL_SUPPORT = "conditional_support"
    NEUTRAL = "neutral"
    CONDITIONAL_OPPOSE = "conditional_oppose"
    OPPOSE = "oppose"


class ArtifactStatus(str, Enum):
    DRAFT = "draft"
    UNDER_REVIEW = "under_review"
    APPROVED = "approved"
    REJECTED = "rejected"


class MotionType(str, Enum):
    """The small, deliberation-oriented subset of Robert's Rules we support."""
    MAIN = "main_motion"
    AMENDMENT = "amendment"
    PROCEDURAL = "procedural"
    INFORMATION = "information_request"


class MotionStatus(str, Enum):
    DRAFT = "draft"
    AWAITING_SECOND = "awaiting_second"
    DEBATE_OPEN = "debate_open"
    AMENDMENT_PENDING = "amendment_pending"
    VOTING = "voting"
    ADOPTED = "adopted"
    REJECTED = "rejected"
    WITHDRAWN = "withdrawn"


class RoleType(str, Enum):
    RESIDENT = "resident"
    VULNERABLE_GROUP = "vulnerable_group"
    EXPERT = "expert"
    MANAGER = "manager"
    BUSINESS = "business"
    MODERATOR = "moderator"


