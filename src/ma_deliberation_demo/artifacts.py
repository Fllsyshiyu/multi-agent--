# ── 9. Agent Contract (SOP v1.2 S3) ──────────────────────────────────────────

@dataclass
class AgentContract:
    """Agent 职责合约 — defines what each agent is responsible for, its inputs,
    outputs, validation rules, and boundaries. Generated at S3 (Role Mapping).

    Per SOP v1.2 §2.1: each agent must have an explicit contract so it knows
    exactly what it should and should not do during deliberation.
    """
    contract_id: str = ""
    agent_id: str = ""
    agent_name: str = ""
    role: str = ""  # archetype / role name
    responsibility: str = ""  # what this agent must deliver
    input_artifacts: list[str] = field(default_factory=list)  # artifact types expected
    output_artifacts: list[str] = field(default_factory=list)  # artifact types to produce
    validation_rules: list[str] = field(default_factory=list)  # rules outputs must satisfy
    boundary_rules: list[str] = field(default_factory=list)  # what agent CANNOT do/say
    artifact_id: str = ""
    stage_id: str = ""
    produced_by: str = ""


# ── 10. Deliberation Plan (SOP v1.2 S5) ──────────────────────────────────────

@dataclass
class DeliberationPlan:
    """议事行动计划 — each inner-circle agent generates this before speaking.

    Per SOP v1.2 §8 (S5): ensures agents enter the round knowing their goal,
    non-negotiables, concessions, questions to others, and evidence to use.
    """
    plan_id: str = ""
    agent_id: str = ""
    agent_name: str = ""
    round_id: int = -1
    round_goal: str = ""  # what to achieve in this round
    core_interest: str = ""  # single most important interest
    non_negotiables: list[str] = field(default_factory=list)  # red lines
    possible_concessions: list[str] = field(default_factory=list)  # areas of flexibility
    question_to_others: str = ""  # question for other agents
    evidence_to_use: list[str] = field(default_factory=list)  # evidence IDs
    artifact_id: str = ""
    stage_id: str = "S5"
    produced_by: str = ""
    input_refs: list[str] = field(default_factory=list)
    validation_status: str = "pending"


